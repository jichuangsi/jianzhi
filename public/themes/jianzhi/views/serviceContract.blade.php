    <p>2.1.5 费用实现：发包方按审核标准确认接包方提交的项目成果合格之后，提交平台结算清单，并向平台指定账户支付结算费用（含任务结算费、平台服务费等），平台根据发包方提供的任务结算清单，从发包方支付的结算费用中划扣平台服务费，然后将任务结算费划至相关接包方的银行账户。</p>
    <p>2.2 接包方账户使用方式：接包方在平台上领取的全部任务以及接包方团队师傅在线下具体执行任务中，均以接包方自身名义进行任务领取、执行、完成、提交等。</p>
