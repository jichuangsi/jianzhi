    <p>1.8 “审核”：接包方按任务要求完成提交后，发包方按任务要求进行审核，并在平台确认验收结果。</p>
    <p>1.9 “结算金额”：是指在平台发布任务或委托服务事项中发生的交易金额（含任务结算费、平台服务费等其他相关费用）。</p>
